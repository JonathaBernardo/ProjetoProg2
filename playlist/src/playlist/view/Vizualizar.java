    private void carregarListaDeMusicas() {
        try (BufferedReader br = new BufferedReader(new FileReader("musicas.txt"))) {
            String linha;
            while ((linha = br.readLine()) != null) {
                String[] campos = linha.split(",");
                if (campos.length >= 5) {
                    tableModel.addRow(new Object[]{campos[0], campos[1], campos[2], campos[3], campos[4]});
                }
            }
        } catch (IOException e) {
            JOptionPane.showMessageDialog(this, "Erro ao ler o arquivo de m√∫sicas.");
            e.printStackTrace();
        }
    }

