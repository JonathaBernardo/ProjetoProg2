    private void carregarDadosMusica() {
        try (BufferedReader br = new BufferedReader(new FileReader("musicas.txt"))) {
            String linha;
            while ((linha = br.readLine()) != null) {
                String[] campos = linha.split(",");

                if (campos.length >= 1 && campos[0].trim().equalsIgnoreCase(nome)) {
                    // Preencha os campos com as informações da música
                    novonome.setText(campos[0]);
                    novoartista.setText(campos[1]);
                    novoano.setText(campos[2]);
                    novoalbum.setText(campos[3]);
                    novogenero.setText(campos[4]);
                    nome = campos[0]; // Defina a variável nome com o nome da música atual
                    break; // Saia do loop, pois a música foi encontrada
                }
            }
        } catch (IOException e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Erro ao ler o arquivo de músicas.");
            e.printStackTrace();
        }
    }
