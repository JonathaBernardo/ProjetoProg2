    private void confirmareditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_confirmareditActionPerformed
        String novoNome = novonome.getText();
        String novoArtista = novoartista.getText();
        String novoAno = novoano.getText();
        String novoAlbum = novoalbum.getText();
        String novoGenero = novogenero.getText();

        // Verifique se algum campo está vazio
        if (novoNome.isEmpty() || novoArtista.isEmpty() || novoAno.isEmpty() || novoAlbum.isEmpty() || novoGenero.isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(this, "Preencha todos os campos.");
            return;
        }

        // Crie uma lista temporária para armazenar as músicas atualizadas
        List<String> musicasAtualizadas = new ArrayList<>();

        try (BufferedReader br = new BufferedReader(new FileReader("musicas.txt"))) {
            String linha;
            while ((linha = br.readLine()) != null) {
                String[] campos = linha.split(",");
                if (campos.length >= 1 && campos[0].trim().equalsIgnoreCase(nome)) {
                    // Se a linha corresponder à música a ser editada, substitua pelos novos valores
                    musicasAtualizadas.add(String.join(",", novoNome, novoArtista, novoAno, novoAlbum, novoGenero));
                } else {
                    // Caso contrário, mantenha a linha original
                    musicasAtualizadas.add(linha);
                }
            }
        } catch (IOException e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Erro ao ler o arquivo de músicas.");
            e.printStackTrace();
        }

        // Escreva as músicas atualizadas de volta no arquivo
        try (BufferedWriter bw = new BufferedWriter(new FileWriter("musicas.txt"))) {
            for (String musica : musicasAtualizadas) {
                bw.write(musica);
                bw.newLine();
            }
            javax.swing.JOptionPane.showMessageDialog(this, "Música editada com sucesso!");
            dispose(); // Feche a tela de edição após a conclusão
        } catch (IOException e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Erro ao salvar as músicas editadas.");
            e.printStackTrace();
        }
    }//GEN-LAST:event_confirmareditActionPerformed

